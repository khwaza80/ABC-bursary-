        document.addEventListener("DOMContentLoaded", function () {
            const today = new Date();
            const weekday = today.toLocaleString('en-ZA', { weekday: 'long', timeZone: 'Africa/Johannesburg' });
            const dateStr = today.toLocaleDateString('en-ZA', {
                weekday: 'long', year: 'numeric', month: 'long',
                day: 'numeric', hour: '2-digit', minute: '2-digit', second: '2-digit',
                timeZone: 'Africa/Johannesburg'
            });
            document.getElementById('liveDate').innerHTML = `<strong>📅 ${dateStr}</strong>`;

            const isClosed = (weekday === "Saturday" || weekday === "Sunday");
            const appStatus = document.getElementById('appStatus');
            appStatus.innerHTML = isClosed
                ? `<p class="closed">🚫 Applications are closed today (${weekday}).</p>`
                : `<div>
                        <a href="login.php" class="btn">🔐 Login</a><br>
                        <a href="register.php" class="btn">🚀 Apply Now / Register</a>
                   </div>`;

            // COUNTDOWN
            const closingDate = new Date("2026-11-30T23:59:59");
            const countdownEls = {
                days: document.getElementById('days'),
                hours: document.getElementById('hours'),
                minutes: document.getElementById('minutes'),
                seconds: document.getElementById('seconds')
            };

            function updateCountdown() {
                const now = new Date();
                const diff = closingDate - now;

                if (diff > 0) {
                    const days = Math.floor(diff / (1000 * 60 * 60 * 24));
                    const hours = Math.floor((diff / (1000 * 60 * 60)) % 24);
                    const minutes = Math.floor((diff / (1000 * 60)) % 60);
                    const seconds = Math.floor((diff / 1000) % 60);
                    countdownEls.days.textContent = days;
                    countdownEls.hours.textContent = hours;
                    countdownEls.minutes.textContent = minutes;
                    countdownEls.seconds.textContent = seconds;
                } else {
                    document.getElementById('countdownContainer').innerHTML =
                        `<strong style="color:#ff4d4d;">🚫 Bursary applications are closed!</strong>`;
                }
            }

            updateCountdown();
            setInterval(updateCountdown, 1000);

            // Scroll to Top Button
            const scrollBtn = document.getElementById("scrollTopBtn");
            window.onscroll = function() {
                if (document.body.scrollTop > 200 || document.documentElement.scrollTop > 200) {
                    scrollBtn.style.display = "block";
                } else {
                    scrollBtn.style.display = "none";
                }
            };
            scrollBtn.onclick = function() {
                window.scrollTo({top: 0, behavior: 'smooth'});
            };

            // Progress Bar
            const closingTime = closingDate.getTime();
            const startTime = new Date("2026-11-31T00:00:00").getTime();
            const totalDuration = closingTime - startTime;
            function updateProgressBar() {
                const now = Date.now();
                const elapsed = now - startTime;
                const progressPercent = Math.min((elapsed / totalDuration) * 100, 100);
                document.querySelector(".progress-bar").style.width = progressPercent + "%";
            }
            updateProgressBar();
            setInterval(updateProgressBar, 1000);

            // THEME TOGGLE (initialize)
            (function initThemeToggle(){
                const saved = localStorage.getItem('abc_theme'); // 'dark' or 'light'
                if (saved === 'dark') {
                    document.documentElement.setAttribute('data-theme', 'dark');
                } else {
                    // preserve original look when not dark: remove attr or set light
                    document.documentElement.removeAttribute('data-theme');
                }
            })();
        });
    </script>
