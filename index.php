        }

        function setDark(dark) {
            if (dark) {
                root.setAttribute('data-theme', 'dark');
                localStorage.setItem('abc_theme', 'dark');
                toggle.setAttribute('aria-pressed', 'true');
            } else {
                root.removeAttribute('data-theme');
                localStorage.setItem('abc_theme', 'light');
                toggle.setAttribute('aria-pressed', 'false');
            }
        }

        toggle.addEventListener('click', function(e){
            const active = root.getAttribute('data-theme') === 'dark';
            setDark(!active);
            // small focus ring for accessibility
            toggle.style.outline = '3px solid rgba(255,255,255,0.12)';
            setTimeout(()=> toggle.style.outline = 'none', 400);
        });

        // keyboard support (Enter / Space)
        toggle.addEventListener('keydown', function(e){
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                toggle.click();
            }
        });
